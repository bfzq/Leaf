;以下是标准FAT12格式软盘专用代码

DB		0xeb, 0x4e, 0x90
DB		"HELLOIPL"		; 启动区的名字，可以任意字符串，7个字节
DW		512				; 每个扇区的大小（必须是512字节）
DB		1				; 簇的大小，必须一个扇区
DW		1				; FAT的起始位置，一般第一个扇区开始
DB		2				; FAT的个数，必须是2
DW		224				; 根目录大小，一般224项
DW		2880			; 磁盘大小，必须2880扇区
DB		0xf0			; 磁盘种类，必须0xf0
DW		9				; FAT长度，必须9扇区
DW		18				; 一个磁道有几个扇区，必须18个
DW		2				; 磁头数，必须2个
DD		0				; 不使用分区，必须是0
DD		2880			; 重写一次磁盘大小
DB		0,0,0x29		; 固定值
DD		0xffffffff		; 可能是卷标号码
DB		"HELLO-OS   "	; 磁盘名字，11字节
DB		"FAT12   "		; 磁盘格式名字，8字节
RESB	18				; 空出18字节

;程序主体

